/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */
  #define MAX_LENGTH 30
  char text[MAX_LENGTH];
  //uint16_t length;
  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_USART3_UART_Init();
  MX_I2C1_Init();
  MX_I2C4_Init();
  MX_TIM1_Init();
  MX_TIM3_Init();
  /* USER CODE BEGIN 2 */
  BMP280_Init(&hi2c1, BMP280_TEMPERATURE_16BIT, BMP280_STANDARD, BMP280_FORCEDMODE);
  lcd_init ();
  lcd_send_string ("Czujnik BMP280");
  HAL_Delay(100);
  lcd_put_cur(1, 0);
  lcd_send_string("I2C1-BMP,I2C4-LCD");
  HAL_Delay(2000);
  HAL_TIM_Encoder_Start(&htim1, TIM_CHANNEL_ALL);
  HAL_TIM_PWM_Start(&htim3, TIM_CHANNEL_3);

  //DODANE
  //__HAL_TIM_SET_COUNTER(&htim1,(uint32_t)temp_zadana);
  //enco_last = (int16_t)temp_zadana;
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
{
    BMP280_ReadTemperatureAndPressure(&temperature, &pressure);

    sprintf((char*)text, "%.2f,\r\n", temperature);
    HAL_UART_Transmit(&huart3, (uint8_t*)text, strlen(text), 1000);

    lcd_clear();
    lcd_put_cur(0, 0);
    sprintf((char*)text, "Temp. %.2f C", temperature);
    lcd_send_string(text);
    float tz_temp = temp_zadana;
    enco = __HAL_TIM_GET_COUNTER(&htim1);
    if (enco != enco_last) {
        if ((enco - enco_last > 10)&&(enco_last == 0)){
        	tz_temp--;
        	enco_last = 29;
        } else if ((enco - enco_last < 0)&&(enco_last == 30)){
        	tz_temp++;
        	enco_last = 0;
        } else {
        	tz_temp += (enco - enco_last) / 2.0f;  // adjust sensitivity
        	enco_last = enco;
        }
    }
    if ((tz_temp < 40)&&(tz_temp > 0)){
    	temp_zadana = tz_temp;
    }
    lcd_put_cur(1, 0);
    sprintf((char*)text, "T_zad. %.2f C", temp_zadana);
    lcd_send_string(text);

    float e = temp_zadana - temperature;
    float P, I;
    P = Kp * e;
    I = I_prev + (e + e_prev);
    I_prev = I;
    I = Ki*I*0.5;

    float u_temp = P+I;
    float u;
    if (u_temp > u_max) {
        u = u_max;
    } else if (u_temp < u_min) {
        u = u_min;
    } else {
        u = u_temp;
    }

    e_prev = e;

    __HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_3, (uint32_t)u);

    HAL_Delay(1000);
}

  /* USER CODE END 3 */
}

